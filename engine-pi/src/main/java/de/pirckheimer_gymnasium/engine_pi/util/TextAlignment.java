package de.pirckheimer_gymnasium.engine_pi.util;

/**
 * Die Textausrichtung wie Buchstaben in einer Textzeile oder einem Textfeld
 * ausgerichtet werden können.
 */
public enum TextAlignment
{
    /**
     * Linksbündig.
     */
    LEFT,
    /**
     * Zentriert.
     */
    CENTER,
    /**
     * Rechtsbündig.
     */
    RIGHT
}
